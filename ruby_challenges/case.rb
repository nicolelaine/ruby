weather = 'sunny'

case weather 
when 'sunny'
	puts "Oh yeah, it's sunny!"
when 'cloudy'
	puts "Oh no, what a shame it's cloudy."
when 'foggy'
	puts "I can't see so well, because it's foggy."
when 'rainy'
	puts "Get out your rain jacket, because it's rainy!"
else 
    puts "I'm not sure what the weather is, so best to be prepared for anything!"
end 